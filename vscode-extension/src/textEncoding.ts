const reverseMaps = new Map<TextEncoding, Map<string, number>>()

function codePointForByte(
  byte: number,
  encoding: TextEncoding
): number | undefined {
  if (byte < 0 || byte > 0xff) {
    return undefined
  }

  switch (encoding) {
    case 'ascii':
      return byte <= 0x7f ? byte : undefined
    case 'windows-1252':
      if (byte >= 0x80 && byte <= 0x9f) {
        return WINDOWS_1252_EXTENSION[byte - 0x80]
      }
      return byte
    case 'cp437':
      return byte < 0x80 ? byte : CP437_HIGH[byte - 0x80]
    case 'ebcdic-037':
      return EBCDIC_037[byte]
    case 'macroman':
      return byte < 0x80 ? byte : MAC_ROMAN_HIGH[byte - 0x80]
  }
}

function reverseMapForEncoding(encoding: TextEncoding): Map<string, number> {
  const existing = reverseMaps.get(encoding)
  if (existing) {
    return existing
  }

  const map = new Map<string, number>()
  for (let byte = 0; byte <= 0xff; byte += 1) {
    const codePoint = codePointForByte(byte, encoding)
    if (codePoint === undefined) {
      continue
    }
    const char = String.fromCodePoint(codePoint)
    if (!map.has(char)) {
      map.set(char, byte)
    }
  }
  reverseMaps.set(encoding, map)
  return map
}

function isControlCodePoint(codePoint: number): boolean {
  return (
    (codePoint >= 0x00 && codePoint <= 0x1f) ||
    (codePoint >= 0x7f && codePoint <= 0x9f)
  )
}

export function decodeTextByte(
  byte: number,
  encoding: TextEncoding
): string | undefined {
  const codePoint = codePointForByte(byte, encoding)
  return codePoint === undefined ? undefined : String.fromCodePoint(codePoint)
}

const HEX2 = '0123456789ABCDEF'

/** Single byte as two uppercase hex digits. Hot path for hex rendering. */
export function byteToHex2(byte: number): string {
  return HEX2[byte >> 4] + HEX2[byte & 0xf]
}

/** Compact uppercase hex string for a byte sequence. */
export function bytesToHex(bytes: ArrayLike<number>): string {
  let out = ''
  for (let index = 0; index < bytes.length; index += 1) {
    out += byteToHex2(bytes[index])
  }
  return out
}

export function isPrintableAscii(byte: number): boolean {
  return byte >= 0x20 && byte <= 0x7e
}

export function isPrintableTextByte(
  byte: number,
  encoding: TextEncoding
): boolean {
  const codePoint = codePointForByte(byte, encoding)
  return codePoint !== undefined && !isControlCodePoint(codePoint)
}

export function classifyTextByte(
  byte: number,
  encoding: TextEncoding
): ByteClass {
  if (byte === 0x00) {
    return 'Null'
  }
  if (isPrintableTextByte(byte, encoding)) {
    return 'Printable'
  }
  if (byte === 0xff) {
    return 'FF'
  }
  if (
    byte < 0x20 ||
    byte === 0x7f ||
    codePointForByte(byte, encoding) !== undefined
  ) {
    return 'Control'
  }
  return 'High-bit'
}

export function formatTextByte(
  byte: number,
  encoding: TextEncoding,
  fallback = '.'
): string {
  return isPrintableTextByte(byte, encoding)
    ? (decodeTextByte(byte, encoding) ?? fallback)
    : fallback
}

export function encodeTextToBytes(
  text: string,
  encoding: TextEncoding
): number[] | undefined {
  const map = reverseMapForEncoding(encoding)
  const bytes: number[] = []
  for (const char of Array.from(text)) {
    const byte = map.get(char)
    if (byte === undefined) {
      return undefined
    }
    bytes.push(byte)
  }
  return bytes
}

export function encodeTextToHex(
  text: string,
  encoding: TextEncoding
): string | undefined {
  const bytes = encodeTextToBytes(text, encoding)
  return bytes
    ?.map((byte) => byte.toString(16).toUpperCase().padStart(2, '0'))
    .join('')
}

export function printableTextToHex(
  text: string,
  encoding: TextEncoding
): string | undefined {
  const bytes = encodeTextToBytes(text, encoding)
  if (!bytes || bytes.some((byte) => !isPrintableTextByte(byte, encoding))) {
    return undefined
  }
  return bytes
    .map((byte) => byte.toString(16).toUpperCase().padStart(2, '0'))
    .join('')
}

export function decodeTextBytes(
  bytes: number[],
  encoding: TextEncoding
): string | undefined {
  const chars: string[] = []
  for (const byte of bytes) {
    const char = decodeTextByte(byte, encoding)
    if (char === undefined || !isPrintableTextByte(byte, encoding)) {
      return undefined
    }
    chars.push(char)
  }
  return chars.join('')
}

/**
 * Unsubscribe from events for the given viewport
 * @param viewport_id viewport to unsubscribe from events
 * @return viewport ID that has been unsubscribed from events, on success
 */
export async function unsubscribeViewport(
  viewport_id: string
): Promise<string> {
  const log = getLogger()
  const request = new UnsubscribeToViewportEventsRequest().setId(viewport_id)
  debugLog(log, () => ({
    fn: 'unsubscribeViewport',
    rqst: request.toObject(),
  }))
  const client = await getClient()
  return new Promise<string>((resolve, reject) => {
    client
      .unsubscribeToViewportEvents(
        request,
        (err, r: UnsubscribeToViewportEventsResponse) => {
          if (err) {
            log.error({
              fn: 'unsubscribeViewport',
              rqst: request.toObject(),
              err: {
                msg: err.message,
                details: err.details,
                code: err.code,
                stack: err.stack,
              },
            })
            return reject(`unsubscribeViewport error: ${err.message}`)
          }
          debugLog(log, () => ({
            fn: 'unsubscribeViewport',
            resp: r.toObject(),
          }))
          return resolve(r.getId())
        }
      )
      .on('error', (err) => {
        // Call cancelled thrown when server is shutdown
        if (!err.message.includes('Call cancelled')) {
          log.error('unsubscribeViewport critical error: ' + err.message)
          throw err
        }
        log.info('unsubscribeViewport error: ' + err.message)
      })
  })
}
